int		ft_digcount_base(unsigned int n, int base)
{
	int		i;

	if (base < 2)
		return (0);
	if (n == 0)
		return (1);
	i = 0;
	while (n > 0)
	{
		n = n / base;
		i++;
	}
	return (i);
}
